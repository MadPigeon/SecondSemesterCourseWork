void sortShowHor(int *array, int n)
{
	for (int i = 0; i < n; i++)
	{		
		cout << array[i] << " ";
	}
	cout << "\n—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—\n";
}

void sortShowVert(int *array, int n)
{
	for (int i = 0; i < n; i++)
	{		
		cout << array[i] << endl;
	}
	cout << "\n—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—-—\n";
}